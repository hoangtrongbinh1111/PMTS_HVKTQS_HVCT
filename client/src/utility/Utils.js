export const convertToUTCTime = (dateString) => {
  const parts = dateString.split("-") // Tách thành các phần ngày, tháng, năm
  const day = parseInt(parts[0], 10) // Ngày
  const month = parseInt(parts[1], 10) - 1 // Tháng (giảm 1 vì tháng trong JavaScript bắt đầu từ 0)
  const year = parseInt(parts[2], 10) // Năm

  const utcDate = new Date(Date.UTC(year, month, day))
  return utcDate.toISOString() // Chuyển thành định dạng UTC time
}

export const toDateTimeString = date => {
  const today = new Date(date)
  const hh = String(today.getHours()).padStart(2, "0")
  const MM = String(today.getMinutes()).padStart(2, "0")
  const dd = String(today.getDate()).padStart(2, "0")
  const mm = String(today.getMonth() + 1).padStart(2, "0")
  const yyyy = today.getFullYear()

  return `${hh}:${MM} ${dd}/${mm}/${yyyy}`
}